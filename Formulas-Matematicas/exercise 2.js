//2. Escreva um programa que calcule a média aritmética de dois números.

// Solicitando os números para o usuário
let num1 = parseFloat(prompt("Digite um número:"));
let num2 = parseFloat(prompt("Digite um número:"));

// Média Aritmética de 2 números
let media = (num1 + num2) / 2;

//mostra as informações na dela
alert(`A média desses dois 2 números é ${media}`);
